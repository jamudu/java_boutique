    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        prendaStock = new Prendas(codigoTxt.getText());
        if (prendaJDBC.existePrenda(prendaStock.getCodigo())) {
            prendaStock = prendaJDBC.selectPrenda(prendaStock.getCodigo());
            decripcionLbl.setText(prendaStock.getDescripcion());
            pCosteLbl.setText(String.valueOf(prendaStock.getPrecioCoste()));
            pVentaLbl.setText(String.valueOf(prendaStock.getPrecioVenta()));
            stockLbl.setText(String.valueOf(prendaStock.getStock()));
            colorLbl.setText(prendaStock.getColor());
            tallaLbl.setText(prendaStock.getTalla());

            operacionBtn.setVisible(true);
            cantidadTxt.setVisible(true);
            cantidadTxt.requestFocusInWindow();
            if (modo.equals("sumaStock")) {
                jLabel11.setText("Cantidad a añadir");
                operacionBtn.setText("sumar");
            } else if (prendaStock.getStock() == 0) {
                operacionBtn.setVisible(false);
                cantidadTxt.setVisible(false);
                jLabel11.setText("No hay stock a restar");
            } else {
                jLabel11.setText("Cantidad a restar");
                operacionBtn.setText("restar");
            }
            cantidadTxt.selectAll();
        } else {
            JOptionPane.showMessageDialog(this, "El codigo " + prendaStock.getCodigo() + " no existe", "Codigo incorrecto", JOptionPane.ERROR_MESSAGE);
            codigoTxt.requestFocusInWindow();
            codigoTxt.selectAll();
        }
    }//GEN-LAST:event_buscarBtnActionPerformed

