    private void operacionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_operacionBtnActionPerformed
        try {
            if (Integer.parseInt(cantidadTxt.getText()) <= 0) {
                mensajeError("Solo numeros enteros positivos");
            } else if (modo.equals("sumaStock")) {
                stockModificado();
                aceptarBtn.setEnabled(true);
                aceptarBtn.requestFocusInWindow();
            } else if (prendaStock.getStock() < Integer.parseInt(cantidadTxt.getText())) {
                mensajeError("La cantidad no puede ser mayor que el stock");
            } else {
                cantidadTxt.setText("-" + cantidadTxt.getText());
                stockModificado();
                aceptarBtn.setEnabled(true);
                aceptarBtn.requestFocusInWindow();
            }
        } catch (NumberFormatException ex) {
            mensajeError("Solo numeros enteros positivos");
        }
    }//GEN-LAST:event_operacionBtnActionPerformed

